"""Server event stream utils"""

class ServerSentEvent(object):
    """SSE "protocol" is described here: http://mzl.la/UPFyxY"""
    def __init__(self, data):
        self.data = data
        self.event = None
        self.id = None
        self.desc_map = {
            self.data : "data",
            self.event : "event",
            self.id : "id"
        }

    def encode(self):
        """Serialise event for transmission"""
        if not self.data:
            return ""
        lines = ["%s: %s" % (v, k)
                 for k, v in self.desc_map.iteritems() if k]

        return "%s\n\n" % "\n".join(lines)
